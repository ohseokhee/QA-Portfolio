# [Day 5] 반복문(for)을 활용한 일괄 처리 실습

# 1. 리스트(List) 만들기 : 대괄호 [] 안에 여러 값을 넣습니다.
user_list = ["석희", "철수", "영희", "길동"]

print("--- 유저별 서버 공지 발송 시작 ---")

# 2. 반복문 실행 : user_list에 있는 이름을 하나씩 'name'이라는 변수에 담아 실행합니다.
for name in user_list:
    print(f"발송 완료 : {name}님, 잠시 후 서버 점검이 시작됩니다.")

print("--- 모든 발송이 완료되었습니다 ---")

# 3. 숫자 범위 반복 : 1부터 5까지 반복 (QA에서 횟수 반복할 때 자주 씀)
print("\n--- 시스템 재시작 카운트다운 ---")
for i in range(1, 6):
    print(f"재시도 중... ({i}/5)")